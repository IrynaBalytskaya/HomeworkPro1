# Выведите на экран прямоугольник из *. Причем, высота и ширина
# прямоугольника вводятся с клавиатуры. Например, ниже представлен
# прямоугольник с высотой 4 и шириной 5.

w = int(input('w = '))
h = int(input('h = '))
for i in range(0, h):
    str = ""
    if i == 0 or i == h - 1:
        for si in range(0, w):
            str += '*'
    else:
        for si in range(0, w):
            if si == 0 or si == w - 1:
                str += '*'
            else:
                str += ' '
    print(str)

