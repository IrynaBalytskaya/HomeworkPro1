# Вычислить с помощью цикла факториал числа n введенного с
# клавиатуры (4<n<16). Факториал числа - это произведение всех чисел от
# этого числа до 1. Например, 5!=5*4*3*2*1=120

n = int(input('n = '))
if n <= 4 or n >= 16:
    print("bed number")
else:
    i = n
    fact = 1
    while i:
        fact = fact * i
        i -= 1
    print(fact)

